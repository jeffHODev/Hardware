/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	static uint8_t ms10;
	static uint8_t ms100;
	
  // 系统时间V
  if(MSecond++>=999)					// 秒
  {
		MSecond=0;								// 10MS
		Second_flage=1;						// 秒标志
		Refresh_LED=1;						// 刷新方法LED
		HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
    // 时钟
    if(Second++>=59)      		// 分钟
    {
      Second=0;								// 秒
			// 执行分钟操作
			if(Fanxi_Delay_Time>0) Fanxi_Delay_Time--;					// 反洗延时时间
			if(Fanxi_Continue_Time>0) Fanxi_Continue_Time--;		// 反洗持续时间
			if(Chongxi_Continue_Time>0) Chongxi_Continue_Time--;// 冲洗持续时间
      if(Minute++>=59)				// 时
      {
        Minute=0;	        		// 分钟复位		
				Hour_Flage=1;					// 小时标志
        Hour++;								// 小时
			}
		}
		// 秒执行操作
		if(Standby_Time_Delay>0) Standby_Time_Delay--;
	}			
	// 10ms标志位
	if(ms10++>9)								// 10ms流量计
	{
		ms10=0;										// 复位
		MS_Flage=1;								// 置位
		if(LLJ_Rest_Time>0) LLJ_Rest_Time--;
		if(LLJ_Rest_Time==1)			// 流量计复位时间
		{
			LLJ_Start_Flage=0;			// 
			LLJ_Now_Value=0;				// 
		}
		if((Key_Now_Mode.KEY_VAL_MODE==KEY_VAL_OPEN)&&(LLJ_Start_Flage==0)&&(Fanxi_Mode==0))
		{
			if(Queshui_Delay_Time++>500)		// 延时2S后
			{
				Queshui_Delay_Time=500;				// 时间最长2S
				LED_QS=1;											// 缺水指示灯点亮
				Queshi_Flage=1;								// 缺水检测
			}
		}
		else {	LED_QS=0;	Queshui_Delay_Time=0;	Queshi_Flage=0;	}
	}// 10ms标志位		
	// 150ms标志
	if(ms100++>149)								// 每150ms执行一次
	{
		ms100=0;										//
		Read_Filter_Flage=1;				//
		// 蜂鸣器
		Speak_Work_Mode();					// 蜂鸣器工作		
	}
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  /* USER CODE END SysTick_IRQn 1 */
}

